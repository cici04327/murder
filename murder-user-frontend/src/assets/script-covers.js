/**
 * 根据剧本名称和类型获取封面图片
 * @param {string} scriptName - 剧本名称
 * @param {string} categoryName - 剧本类型
 * @returns {string} 图片URL
 */
export function getScriptCover(scriptName, categoryName) {
  // 优先使用剧本名称匹配
  if (SCRIPT_COVERS[scriptName]) {
    return SCRIPT_COVERS[scriptName]
  }
  
  // 其次使用类型默认封面
  if (SCRIPT_COVERS.default[categoryName]) {
    return SCRIPT_COVERS.default[categoryName]
  }
  
  // 最后返回通用默认封面
  return SCRIPT_COVERS.default['本格推理']
}

